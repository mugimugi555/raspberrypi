#!/usr/bin/bash

# wget https://raw.githubusercontent.com/mugimugi555/raspberrypi/main/install_bullseye.sh && bash install_bullseye.sh ;

#-----------------------------------------------------------------------------------------------------------------------
# backup repository
#-----------------------------------------------------------------------------------------------------------------------
cp /etc/apt/sources.list ~/sources.list ;
cp /etc/apt/sources.list.d/raspi.list ~/raspi.list ;

#-----------------------------------------------------------------------------------------------------------------------
# write repository
#-----------------------------------------------------------------------------------------------------------------------
sudo sed -i 's/buster/bullseye/g' /etc/apt/sources.list ;
sudo sed -i 's/buster/bullseye/g' /etc/apt/sources.list.d/raspi.list ;

#-----------------------------------------------------------------------------------------------------------------------
# before install gcc-8-base
#-----------------------------------------------------------------------------------------------------------------------
sudo apt update ;
sudo apt install -y gcc-8-base ;

#-----------------------------------------------------------------------------------------------------------------------
# upgrade simulate
#-----------------------------------------------------------------------------------------------------------------------
apt --simulate upgrade -y ;

#-----------------------------------------------------------------------------------------------------------------------
# upgrade
#-----------------------------------------------------------------------------------------------------------------------
sudo apt upgrade -y ;
sudo apt dist-upgrade -y ;
sudo apt autoremove -y ;

#-----------------------------------------------------------------------------------------------------------------------
# finish
#-----------------------------------------------------------------------------------------------------------------------
sudo reboot now ;

